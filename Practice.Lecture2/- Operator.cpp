#include <iostream>
using namespace std;

int main() {

   // Store cost price and selling price
    double cost_price = 19.0;
    double selling_price = 27.0;
    
    // Calculate profit (or loss)
    double profit = selling_price - cost_price;
    
    // Display the result
    cout << profit << endl;
    
    return 0;
}
